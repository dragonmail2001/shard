package org.cloudy.ddl.entity;

public class TBLEntity {
	private Integer wr;
	private Integer dsn;
	private String rule;

	public Integer getWr() {
		return wr;
	}

	public void setWr(Integer wr) {
		this.wr = wr;
	}

	public Integer getDsn() {
		return dsn;
	}

	public void setDsn(Integer dsn) {
		this.dsn = dsn;
	}

	public String getRule() {
		return rule;
	}

	public void setRule(String rule) {
		this.rule = rule;
	}

}
